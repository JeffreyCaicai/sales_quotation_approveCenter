const dictionaries = { en, "zh-CN": zhCN } satisfies Record<AdminLocale, AdminDictionary>;

export type AdminTranslationKey = keyof typeof en;
export type AdminTranslate = (key: AdminTranslationKey, params?: Record<string, string | number>) => string;

interface LocaleStorage {
  getItem(key: string): string | null;
  setItem(key: string, value: string): void;
}

export function readPersistedAdminLocale(storage: Pick<LocaleStorage, "getItem">): AdminLocale {
  return storage.getItem(ADMIN_LOCALE_STORAGE_KEY) === "zh-CN" ? "zh-CN" : "en";
}

export function writePersistedAdminLocale(
  storage: Pick<LocaleStorage, "setItem">,
  locale: AdminLocale,
): void {
  storage.setItem(ADMIN_LOCALE_STORAGE_KEY, locale);
}

export function translateAdmin(
  locale: AdminLocale | undefined,
  key: AdminTranslationKey,
  params: Record<string, string | number> = {},
): string {
  return dictionaries[locale ?? "en"][key].replaceAll(/\{([A-Za-z0-9_]+)\}/g, (placeholder, name: string) =>
    Object.hasOwn(params, name) ? String(params[name]) : placeholder);
}
